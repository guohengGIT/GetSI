package hpe.bean;

public class MemoryIformation {
	private long memoryTotal;
	private long memoryUsed;
	private long memoryFree;
	private long exchange_area_total;
	private long exchange_area_used;
	private long exchange_area_free;
	public long getMemoryTotal() {
		return memoryTotal;
	}
	public void setMemoryTotal(long memoryTotal) {
		this.memoryTotal = memoryTotal;
	}
	public long getMemoryUsed() {
		return memoryUsed;
	}
	public void setMemoryUsed(long memoryUsed) {
		this.memoryUsed = memoryUsed;
	}
	public long getMemoryFree() {
		return memoryFree;
	}
	public void setMemoryFree(long memoryFree) {
		this.memoryFree = memoryFree;
	}
	public long getExchange_area_total() {
		return exchange_area_total;
	}
	public void setExchange_area_total(long exchange_area_total) {
		this.exchange_area_total = exchange_area_total;
	}
	public long getExchange_area_used() {
		return exchange_area_used;
	}
	public void setExchange_area_used(long exchange_area_used) {
		this.exchange_area_used = exchange_area_used;
	}
	public long getExchange_area_free() {
		return exchange_area_free;
	}
	public void setExchange_area_free(long exchange_area_free) {
		this.exchange_area_free = exchange_area_free;
	}
	
	
	
}
